    private void cboColoresItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboColoresItemStateChanged
        // TODO add your handling code here:
       String color=cboColores.getSelectedItem().toString();
       System.out.println("Color" + color);
       
       switch(color.toLowerCase()){ ///para convertir todo a minusculas
           case "rojo":
                panelColores.setBackground(Color.red);
                break;
           case "azul":
                panelColores.setBackground(Color.blue);
                break;
           case "verde":
                panelColores.setBackground(Color.green);
                break;
           case "amarillo":
                panelColores.setBackground(Color.yellow);
                break;
           case "rosa":
                panelColores.setBackground(Color.pink);
                break;
           case "negro":
                panelColores.setBackground(Color.black);
                break;
           case "blanco":
                panelColores.setBackground(Color.white);
                break;
          case "morado":
                panelColores.setBackground(new Color(255,0,0));  //rojo //verde//azul
                break;
       }
      
    }//GEN-LAST:event_cboColoresItemStateChanged

